    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      
        String AcademicYearSemester = academicbox.getText();
        String Programme = programmebox.getSelectedItem().toString();
        String GroupNo = gnbox.getSelectedItem().toString();
        String SubGroupNo = sgnbox.getSelectedItem().toString();
        String GroupID = gibox.getText();
        String SubGroupID= sgibox.getText();
        
   
           
        
        try {
         
        String q = "INSERT INTO addstudentgroup (AcademicYearSemester,Programme,GroupNo,SubGroupNo,GroupID,SubGroupID) values ('"+AcademicYearSemester+"', '"+Programme+"', '"+GroupNo+"', '"+SubGroupNo+"','"+GroupID+"', '"+SubGroupID+"')";
       pst = (PreparedStatement) con.prepareStatement(q);
        pst.execute();
        JOptionPane.showMessageDialog(this, "Successfully added!");
        
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

